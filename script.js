// State
let displayedPosts = 3;
let currentFilter = 'all';
let currentSearch = '';

// ===== RENDER CARDS =====
function renderBlogCards() {
    const grid = document.getElementById('blogGrid');
    const filtered = getFilteredPosts();
    const toShow = filtered.slice(0, displayedPosts);

    if (toShow.length === 0) {
        grid.innerHTML = `<div style="grid-column:1/-1;text-align:center;padding:3rem;border:3px solid #0a0a0a;background:#FFE500;box-shadow:6px 6px 0 #0a0a0a;font-weight:700;font-size:1.1rem;">Gak ada artikel yang cocok 🤔</div>`;
        return;
    }

    grid.innerHTML = toShow.map(post => `
        <div class="blog-card" onclick="openModal(${post.id})">
            <img src="${post.image}" alt="${post.title}" class="blog-image" loading="lazy">
            <div class="blog-content">
                <span class="blog-category" data-cat="${post.category}">${post.category}</span>
                <h3 class="blog-title">${post.title}</h3>
                <p class="blog-excerpt">${post.excerpt}</p>
                <div class="blog-meta">
                    <span>${post.date}</span>
                    <span>By ${post.author}</span>
                </div>
            </div>
        </div>
    `).join('');

    // Show/hide load more
    const loadBtn = document.getElementById('loadMoreBtn');
    loadBtn.style.display = filtered.length > displayedPosts ? 'inline-block' : 'none';
}

function getFilteredPosts() {
    return blogPosts.filter(post => {
        const matchCat = currentFilter === 'all' || post.category === currentFilter;
        const matchSearch = post.title.toLowerCase().includes(currentSearch.toLowerCase()) ||
                            post.excerpt.toLowerCase().includes(currentSearch.toLowerCase());
        return matchCat && matchSearch;
    });
}

// ===== FILTER =====
document.querySelectorAll('.filter-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        currentFilter = btn.dataset.category;
        displayedPosts = 3;
        renderBlogCards();
    });
});

// ===== SEARCH =====
document.getElementById('searchInput').addEventListener('input', (e) => {
    currentSearch = e.target.value;
    displayedPosts = 3;
    renderBlogCards();
});

// ===== LOAD MORE =====
document.getElementById('loadMoreBtn').addEventListener('click', () => {
    displayedPosts += 3;
    renderBlogCards();
});

// ===== MODAL =====
function openModal(id) {
    const post = blogPosts.find(p => p.id === id);
    if (!post) return;

    document.getElementById('modalBody').innerHTML = `
        <h2 class="modal-title">${post.title}</h2>
        <p class="modal-meta">${post.date} &nbsp;·&nbsp; By ${post.author} &nbsp;·&nbsp; <span style="text-transform:uppercase;font-weight:700">${post.category}</span></p>
        <div class="modal-body">${post.content}</div>
    `;

    document.getElementById('articleModal').style.display = 'block';
    document.body.style.overflow = 'hidden';
}

document.getElementById('closeModal').addEventListener('click', closeModal);
document.getElementById('articleModal').addEventListener('click', (e) => {
    if (e.target === e.currentTarget) closeModal();
});
document.addEventListener('keydown', (e) => { if (e.key === 'Escape') closeModal(); });

function closeModal() {
    document.getElementById('articleModal').style.display = 'none';
    document.body.style.overflow = '';
}

